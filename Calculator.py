# function to adds two numbers
def add(x, y):
    return x + y

# function to subtracts two numbers
def subtract(x, y):
    return x - y

# function to multiplies two numbers
def multiply(x, y):
    return x * y

# function to divides two numbers
def divide(x, y):
    return x / y
# function to add to numbers and then subtract a new value from it
def cal(x, y, z):
    return (x + y) - z


